// The indexOf() built-in function is used to tell us what is the position of an element 
// As an example we say: 
const myArr = ['Batman', 'Superman', 'Joker'];
myArr.indexOf('Batman'); // This will print ( 0 (ZERO) ) which is the position of that element in this array
const word = 'World'; 
word.indexOf('o'); // this will print ( 1 ( ONE )) which is the position of that letter in this variable 
