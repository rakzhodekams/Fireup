// The builtin join() method is used to print all elements inside an array as 1 unique string element
// As an example we say: 
const myArr = ['Superman', 'Batman', 'Joker', 'Splinter']; 
myArr.join(); // This will result in: 'Superman, Batman, Joker, Splinter'
myArr.join(' & '); // This will result in: 'Superman & Batman & Joker & Splinter' 
